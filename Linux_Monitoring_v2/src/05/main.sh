#!/bin/bash

# Проверка наличия параметра
if [ $# -ne 1 ]; then
    echo "Использование: $0 <параметр>"
    echo "Параметры:"
    echo "  1 — все записи, отсортированные по коду ответа"
    echo "  2 — все уникальные IP, встречающиеся в записях"
    echo "  3 — все запросы с ошибками (код ответа — 4хх или 5хх)"
    echo "  4 — все уникальные IP, которые встречаются среди ошибочных запросов"
    exit 1
fi

# Переменная для выбора действия
mode=$1

# Обработка логов в зависимости от параметра
case $mode in
    1)
        # Все записи, отсортированные по коду ответа
        echo "Все записи, отсортированные по коду ответа:"
        awk '{print $0}' nginx_*.log | sort -k9,9n  # Исправлено на 9-й столбец для кода ответа
        ;;
    2)
        # Все уникальные IP, встречающиеся в записях
        echo "Все уникальные IP:"
        awk '{print $1}' nginx_*.log | sort | uniq
        ;;
    3)
        # Все запросы с ошибками (код ответа — 4хх или 5хх)
        echo "Все запросы с ошибками (4хх или 5хх):"
        awk '$9 ~ /^4/ || $9 ~ /^5/ {print $0}' nginx_*.log  # Исправлено на 9-й столбец для кода ответа
        ;;
    4)
        # Все уникальные IP, которые встречаются среди ошибочных запросов
        echo "Все уникальные IP среди ошибочных запросов:"
        awk '$9 ~ /^4/ || $9 ~ /^5/ {print $1}' nginx_*.log | sort | uniq  # Исправлено на 9-й столбец для кода ответа
        ;;
    *)
        echo "Неверный параметр. Допустимые значения: 1, 2, 3, 4."
        exit 1
        ;;
esac